#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	if (n < 0 && n != -2147483648)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n >= 0 && n < 10)
		ft_putchar_fd(n + 48, fd);
	else if (n != -2147483648)
	{
		ft_putnbr_fd(n / 10, fd);
		n = n % 10 + 48;
		ft_putchar_fd(n, fd);
	}
}
