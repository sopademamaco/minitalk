#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;
	char	n;

	i = 0;
	n = '\n';
	if (!s || !fd)
		return ;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &n, 1);
}
